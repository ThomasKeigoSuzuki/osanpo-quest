export function getShinakoDialogue(
  bondLevel: number,
  dayOfWeek: number,
  displayName?: string
): string {
  const level = Math.min(7, Math.max(1, bondLevel));
  const dialogues = BOND_DIALOGUES[level] || BOND_DIALOGUES[1];
  const text = dialogues[dayOfWeek % 7];
  const name = displayName || "ぼうけんしゃ";
  return text.replace(/\{name\}/g, name);
}
